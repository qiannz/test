<script type="text/javascript">
var mobile_rule = /^1[0-9]{10}$/;

function verify_mobile(mobile){
	var name = $('#name').val();
	var code = $('#code').val();
	if(name == '') {
		$('#tip').html('<font color="red">朋友们喜欢叫您</font>');
		return false;
	}
	if(!mobile_rule.test(mobile)){
		$('#tip').html('<font color="red">请输入正确的手机号码</font>');
		return false;
	}
	if(code == '') {
		$('#tip').html('<font color="red">请输入验证码</font>');
		return false;
	}else{
		$.post('/active/act/verify-find-phone',{mobile:mobile, code:$('#code').val(), name:name},function(data){
			if(data == 'ok'){
				$('form#phonepassword').submit();
				$('form#phonepassword').attr('disabled', true);
			}else if(data == '-5'){
				$('#tip').html('<font color="red">验证码错误</font>');
				return false;
			}
		});
	}
}

function verify_mobile_valid(name, mobile, overdue){
	if (name == '') {
		$('#tip').html('<font color="red">朋友们喜欢叫您</font>');
		return false;
	}else if(!mobile_rule.test(mobile)){
		$('#tip').html('<font color="red">请输入正确的手机号码</font>');
		return false;
	}else if (overdue == 0) {
		$('#tip').html('<font color="red">该活动已经结束啦</font>');
		return false;
	}else{
		CountDown({
			id:"getCode",
			setFn:function(){ 
				$.post('/active/act/send-phone-message', {mobile:mobile}, function(data){
					if(data == 'ok'){
						$('#tip').html('<font color="red">短信发送成功，请查看您的手机</font>');
					} else if(data == -4) {
						alert('活动名额已满，请期待第二波活动');
					}else if(data == '911') {
						alert('活动名额已满，请期待下一次活动');
					}
				});
			}
		});	
	}
}

$('#phone_submit').bind('click', function(){
	verify_mobile($('#phone').val());
});

$("#getCode").bind("click",function(){
	verify_mobile_valid($('#name').val(), $('#phone').val(), {{$overdue}});
});

function jumpTo(mobile) {
	location.href='/active/act/share/mobile/'+mobile+'/t/f';
}
